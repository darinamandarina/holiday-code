import turtle
tina=turtle.Turtle()
tina.shape('turtle')
def happy_NY():
  tina.penup()
  tina.color('red')
  tina.goto(-50, 100)
  tina.write("Wish you Happy New Year!")
def christmas(x=0, y=0):
    tina.penup()
    tina.color('green')
    tina.goto(x, y)
    tina.pendown()
    for x in range(0,50,10):
      tina.begin_fill() 
      for y in range(0,50,10):
        tina.goto(x + 10, y)
        tina.goto(x + 10, y + 10)
        tina.goto(x - 10, y + 10)
        tina.goto(x - 10, y)
        tina.goto(x, y)
      tina.end_fill()
    tina.begin_fill()
    tina.goto(0, 0)
    tina.goto(-50 , 0)
    tina.goto(-50, 10)
    tina.goto(0,10)
    
    tina.goto(0, 0)
    tina.goto(90,0)
    tina.goto(90,10)
    tina.goto(0,10)
    
    tina.goto(0, 10)
    tina.goto(-40 , 10)
    tina.goto(-40, 20)
    tina.goto(0, 20)
    
    tina.goto(0, 10)
    tina.goto(50+30 , 10)
    tina.goto(50+30, 20)
    tina.goto(0, 20)
    
    tina.goto(0, 20)
    tina.goto(-30 , 20)
    tina.goto(-30, 30)
    tina.goto(0, 30)
    
    tina.goto(0, 20)
    tina.goto(50+20 , 20)
    tina.goto(50+20, 30)
    tina.goto(0, 30)
    
    tina.goto(0, 30)
    tina.goto(-20 , 30)
    tina.goto(-20, 40)
    tina.goto(0, 40)
    
    tina.goto(0, 30)
    tina.goto(50+10 , 30)
    tina.goto(50+10, 40)
    tina.goto(0, 40)    
    
    tina.goto(5, 50)
    tina.goto(5 , 60)
    tina.goto(35, 60)
    tina.goto(35, 50)
    #end of tree
    tina.end_fill()   
    
    tina.goto(20, 60)    
    # beginning of star
    tina.color('black')
    tina.goto(20, 60)
    tina.goto(20 , 75)
    tina.color("white") 
    tina.goto(35, 70)
    tina.color('red')
    tina.begin_fill() 
    tina.goto(15, 100)
    tina.goto(0, 70)
    tina.goto(35, 85)
    tina.goto(0, 85)
    tina.goto(35, 70)
    tina.end_fill()   
    #penek
    tina.color("white") 
    tina.goto(35, 70)
    tina.goto(35, 50)
    tina.color("green")
    tina.goto(15, 0)
    tina.begin_fill() 
    tina.color("brown")
    tina.goto(15, 0)
    tina.goto(15,-10)
    tina.goto(25,-10)
    tina.goto(25, 0)
    tina.end_fill() 
    tina.pendown()
christmas(0,0)
happy_NY()

